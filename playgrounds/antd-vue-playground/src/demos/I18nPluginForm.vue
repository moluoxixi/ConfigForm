}

function renderTemplate(template: string, params?: Record<string, unknown>) {
  return template.replace(/\{([^}]+)\}/g, (_, key: string) => {
    const value = params?.[key.trim()]
    return value == null ? '' : String(value)
  })
}

function t(key: string, params?: Record<string, unknown>) {
  const message = messages[locale.value]?.[key]
  if (message === undefined)
    throw new Error(`Missing local i18n message: ${key}`)
  if (typeof message !== 'string')
    throw new TypeError(`Local i18n helper only supports string messages: ${key}`)

  return renderTemplate(message, params)
}

const runtime = createFormRuntime({
  extensions: [
    createI18nPlugin({
      locale: () => locale.value,
      messages,
      translate: (key, params, defaultMessage, context) => {
        if (key === 'i18n.custom.label') {
          const prefix = context.locale === 'en-US' ? 'Custom field' : '自定义字段'
          const fieldName = params?.field ?? context.field?.field
          if (fieldName == null)
            throw new Error(defaultMessage ?? `Missing custom field name: ${key}`)

          return `${prefix}: ${fieldName}`
        }
        return undefined
      },
    }),
  ],
})

const nextLocaleLabel = computed(() => locale.value === 'zh-CN'
  ? t('i18n.locale.en')
  : t('i18n.locale.zh'))

const fields = computed(() => [
  defineField({
    field: 'username',
    label: i18n('i18n.username.label', '用户名', { max: 20, min: 2 }),
    validateOn: ['blur', 'change'],
    schema: z.string()
      .min(2, t('i18n.username.min', { min: 2 }))
      .max(20, t('i18n.username.max', { max: 20 })),
    span: 12,
    component: Input,
    ...v,
    props: { allowClear: true, placeholder: i18n('i18n.username.placeholder', '请输入用户名') },
    validator: (value, values) => {
      if (values.role === 'guest' && value.length < 4)
        return t('i18n.username.guestMin', { min: 4 })

      return value.includes(' ') ? t('i18n.username.noSpace') : undefined
    },
    transform: value => value.trim(),
  }),
  defineField({
    field: 'email',
    label: i18n('i18n.email.label', '邮箱'),
    validateOn: 'blur',
    schema: z.string().email(t('i18n.email.invalid')).optional(),
    span: 12,
    component: Input,
    ...v,
    props: { allowClear: true, placeholder: i18n('i18n.email.placeholder', '请输入邮箱') },
    transform: value => value?.trim(),
  }),
  defineField({
    field: 'role',
    label: i18n('i18n.role.label', '角色'),
    schema: z.string().min(1, t('i18n.role.required')).optional(),
    span: 12,
    component: Select,
    ...v,
    props: {
      allowClear: true,
      options: [
        { label: i18n('i18n.role.admin', '管理员'), value: 'admin' },
        { label: i18n('i18n.role.user', '用户'), value: 'user' },
        { label: i18n('i18n.role.guest', '访客'), value: 'guest' },
      ],
      placeholder: i18n('i18n.role.placeholder', '请选择角色'),
    },
  }),
  defineField({
    field: 'phone',
    label: i18n('i18n.phone.label', '手机号'),
    schema: z.string().optional(),
    span: 12,
    component: Input,
    ...v,
    props: { allowClear: true, placeholder: i18n('i18n.phone.placeholder', '请输入手机号') },
  }),
  defineField({
    field: 'gender',
    label: i18n('i18n.gender.label', '性别'),
    schema: z.string().optional(),
    span: 12,
    component: RadioGroup,
    ...v,
    slots: {
      default: [
        { component: Radio, props: { value: 'male' }, slots: { default: i18n('i18n.gender.male', '男') } },
        { component: Radio, props: { value: 'female' }, slots: { default: i18n('i18n.gender.female', '女') } },
        { component: Radio, props: { value: 'other' }, slots: { default: () => i18n('i18n.gender.other', '其他') } },
      ],
    },
  }),
  defineField({
    field: 'suggestion',
    label: i18n('i18n.suggestion.label', '建议'),
    schema: z.string().optional(),
    span: 12,
    component: Input,
    ...v,
    props: { allowClear: true, placeholder: i18n('i18n.suggestion.placeholder', '请输入建议') },
  }),
  defineField({
    field: 'custom',
    label: i18n('i18n.custom.label', '自定义字段', { field: 'custom' }),
    schema: z.string().optional(),
    span: 12,
    component: Input,
    ...v,
    props: { allowClear: true, placeholder: i18n('i18n.custom.placeholder', '自定义 translate') },
  }),
])

function toggleLocale() {
  locale.value = locale.value === 'zh-CN' ? 'en-US' : 'zh-CN'
}

function onSubmit(values: Record<string, unknown>) {
  alert(`${t('i18n.feedback.submitSuccess')}\n${JSON.stringify(values, null, 2)}`)
}

function onError(errors: Record<string, string[]>) {
  console.error(t('i18n.feedback.validationFailed'), errors)
}

function onModelUpdate(vals: Record<string, unknown>) {
  Object.assign(formValues, vals)
}
</script>

<template>
  <div>
    <ConfigForm
      ref="formRef"
      :model-value="formValues"
      namespace="moluoxixi"
      :fields="fields"
      :runtime="runtime"
      label-width="120px"
      @submit="onSubmit"
      @error="onError"
      @update:model-value="onModelUpdate"
    />

    <div class="demo-actions">
      <a-button @click="toggleLocale">
        {{ t('i18n.action.switchLocale', { locale: nextLocaleLabel }) }}
      </a-button>
      <a-button type="primary" @click="formRef?.submit()">
        {{ t('i18n.action.submit') }}
      </a-button>
      <a-button @click="formRef?.validate()">
        {{ t('i18n.action.validate') }}
      </a-button>
      <a-button @click="formRef?.reset()">
        {{ t('i18n.action.reset') }}
      </a-button>
    </div>

    <a-divider>{{ t('i18n.preview.title') }}</a-divider>
    <pre class="value-preview">{{ JSON.stringify(formValues, null, 2) }}</pre>
  </div>
</template>

